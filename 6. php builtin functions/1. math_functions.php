<?php
   //php built-in math functions

   //pow
   echo pow(2,7);     //prints 2^7
   echo '<br>';

   //rand
   echo rand(1,100);  //prints random number between 1 to 100
   echo '<br>';

   //sqrt
   echo sqrt(100);   //prints square root of 100
   echo '<br>';

   //ceil
   echo ceil(4.6);   //prints ceiling value of floating number.
   echo '<br>';

   //floor
   echo floor(4.6);  //prints floor value of a floating number
   echo '<br>';

   //max 
   echo max(20,30);       //prints maximum of 2 numbers
   echo '<br>';
   echo max([2,1,5,4,3]); //prints maximum number in a array
   echo '<br>';
   
   //min
   echo min(20,30);       //prints minimum of 2 numbers
   echo '<br>';
   echo min([2,1,5,4,3]); //prints minimum number in a array
   echo '<br>';

   //! Link to find all math functions - php.net/manual/en/ref.math.php
?>